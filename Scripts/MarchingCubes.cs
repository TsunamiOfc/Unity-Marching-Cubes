// these are necessary for the mesh generation
[RequireComponent(typeof(MeshFilter))]
[RequireComponent(typeof(MeshRenderer))]
public class MarchingCubes : MonoBehaviour
{
    public int width, height, length;

    public float surfaceLevel;

    public float perlinMultiplier;

    public Vector3 chunkOffset;

    public float octaveMultiplier;

    public float octavePerlinMultiplier;

    public Material material;

    private float[,,] points;
    private List<Vector3> vertices = new List<Vector3>();
    private List<int> triangles = new List<int>();

    public bool shouldTerraform = false;

    private void Start()
    {
        //Generate();
    }

    private void OnValidate()
    {
        //Generate();
    }

    private void Update()
    {
        if (Input.GetKeyDown(KeyCode.Space))
            Regenerate();
    }

    // generates the mesh
    public void Generate()
    {
        ClearMeshData();

        SetPointsArray();
        SetPoints();

        CreateMeshData();
        CreateMesh();
    }

    // regenerates the mesh
    private void Regenerate()
    {
        ClearMeshData();
        CreateMeshData();
        CreateMesh();
    }

    // clears the triangles and vertices lists
    private void ClearMeshData()
    {
        vertices.Clear();
        triangles.Clear();
    }

    // creates a mesh using the vertices and triangles
    private void CreateMesh()
    {
        Mesh mesh = new Mesh();
        mesh.name = "Terrain Mesh";

        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();

        mesh.RecalculateNormals();

        GetComponent<MeshFilter>().mesh = mesh;
        GetComponent<MeshCollider>().sharedMesh = mesh;
        GetComponent<MeshRenderer>().material = material;
    }

    // sets the points array
    private void SetPointsArray()
    {
        points = new float[width + 1, height + 1, length + 1];
    }

    // sets the points using perlin noise
    private void SetPoints()
    {
        // loop through all the "avaliable" points
        for (int x = 0; x < width + 1; x++)
        {
            for (int y = 0; y < height + 1; y++)
            {
                for (int z = 0; z < length + 1; z++)
                {
					// here you would set the points of the world to a value based on some sort of perlin noise
					// this is a simple way to do that
					float noise = Mathf.PerlinNoise(x * 0.1f, x * 0.1f);
					points[x, y, z] = (-x + noise * perlinMultiplier);

				}
            }
        }
    }

    // returns the float value of a point in the points array based on the point given
    private float SamplePoint(Vector3Int point)
    {
        return points[point.x, point.y, point.z];
    }

    // returns the configuration index for the triangulation table based on the cube passed in
    private int GetConfigIndex(float[] cube)
    {
        // stores the configuration index for the cube
        int cubeIndex = 0;

        // loop through the corners or points of the cube
        for (int i = 0; i < 8; i++)
        {
            // checking if the current corner or point of the cube is above the surface level
            if (cube[i] <= surfaceLevel)
            {
                // if points 0, 1, and 3 are above the surface area, it would look like this (x means not)
                // x, x, x, x, 3, x, 1, 0
                // if this is turned into binary, it would look like this
                // 00001011 which turns into 11, so we use that number to look up in the triangulation table
                // this is what this function does
                cubeIndex |= 1 << i;
            }
        }

        // return the now completed configuration index
        return cubeIndex;
    }

    // calls the MarchCube funtion and passes in a position
    private void CreateMeshData()
    {
        // loop through the points in the 3D space
        for (int x = 0; x < width; x++)
        {
            for (int y = 0; y < height; y++)
            {
                for (int z = 0; z < length; z++)
                {
                    // call the MarchCube function passing in the current position
                    MarchCube(new Vector3Int(x, y, z));
                }
            }
        }
    }

    // handles checking a cube (8 points) and adding the vertices on the edges based on the "active" points
    private void MarchCube(Vector3Int cubePos)
    {
        // create the "cube" which is just an array containing 8 floats, 1 for each corner
        float[] cube = new float[8];

        // loop through the corners or points of the cubes
        for (int i = 0; i < 8; i++)
        {
            // sample each corner or point of the cube based of the points array
            cube[i] = SamplePoint(cubePos + Tables.CornerTable[i]);
        }

        // getting the configuration index for the triangulation table for the cube
        int configIndex = GetConfigIndex(cube);
        // checking if all the points or corners in the cube are above or below the surface level
        // if so return, because no triangles should be drawn for these indexes
        if (configIndex == 0 || configIndex == 255)
            return;

        // this will be responsible for keeping track of the current edge
        int edgeIndex = 0;
        // loop through the triangles, there are never more than 5 triangles in a cube so loop through 5 times
        for (int t = 0; t < 5; t++)
        {
            // loop through the points on the triangle, there are 3 points in each triangle so loop through 3 times
            for (int p = 0; p < 3; p++)
            {
                // creating the indice for current point in the current triangle
                // this will represent which vertex in the triangulation table will be part of the current triangle
                int indice = Tables.TriangleTable[configIndex, edgeIndex];

                // checking if the indice if -1, if it is this means the triangles are done, no more should be created
                // so simple return
                if (indice == -1)
                    return;

                // these represent the indices of the corner points that make up an edge
                Vector3 ind1 = cubePos + Tables.CornerTable[Tables.EdgeIndexes[indice, 0]];
                Vector3 ind2 = cubePos + Tables.CornerTable[Tables.EdgeIndexes[indice, 1]];

                // get the value of both ends of the current edge
                float sample1 = cube[Tables.EdgeIndexes[indice, 0]];
                float sample2 = cube[Tables.EdgeIndexes[indice, 1]];
                // get the difference between these 2 values
                float difference = sample2 - sample1;

                // check if the difference is 0, if it is that means the terrain should pass through the middle
                if (difference == 0f)
                    difference = 0.5f;
                else // else do this fancy little calculation
                    difference = (0.5f - sample1) / difference;

                // calculate the point on the edge of which the terrain actually passes through
                Vector3 vertexPos = ind1 + ((ind2 - ind1) * difference);

                // add the vertex position (the position at which the terrain passes through the cube) to the vertices list
                // and also add the triangles
                vertices.Add(vertexPos);
                triangles.Add(vertices.Count - 1);

                // increment the current index at the end of the loop so we will continue on in the triangle
                edgeIndex++;
            }
        }
    }

    // handles terrain terraforming
    public void Terraform(bool build, Vector3 hitPoint, float brushRadius, float speed, float weigth)
    {
        Debug.Log("Terraform");

        // change this when working with infinte terrain
        if (!shouldTerraform)
            return;

        bool shouldRegenerate = false;

        // loop through the world points
        for (int x = 0; x < width + 1; x++)
        {
            for (int y = 0; y < height + 1; y++)
            {
                for (int z = 0; z < length + 1; z++)
                {
                    // get the distance from the current point and the hit point
                    float distance = Vector3.Distance(hitPoint - chunkOffset, new Vector3(x, y, z));
                    // check if the point is within the brush radius relative to the hit point
                    if (distance <= brushRadius)
                    {
                        shouldRegenerate = true;

                        if (build)
                            points[x, y, z] -= speed * weigth * Time.deltaTime;
                        else if (!build)
                            points[x, y, z] += speed * weigth * Time.deltaTime;
                    }
                }
            }
        }

        if (shouldRegenerate)
            Regenerate();
    }
}

